项目名称：酒仙网

主页：头部部分有二级内容，接口实现搜索，动态生成二级目录
    主轮播图,tab选项卡,主内容动态生成,多个小轮播图，根据后台数据实现倒计时
注册：判断你输入的姓名，密码和验证码是否符合要求，并且有提示，成功失败都有提示，判断用户名，密码是否重复，成功跳入主页面显示用户名
登录页面：判断用户名和密码是否和存在，成功后跳入主页面
商品详情页：动态生成详情页内容，放大镜效果，点击购物车会将商品以抛物线的形式添加入右侧边栏的购物车，右侧边栏可以进行购物车的增删改查，并有个数和价格统计，双击弹出可进入主页或购物车页面
购物车页：商品的增删改查，商品个数和价格统计，并且有商品的缩略(点击个数统计右边的向上按钮，可进行删除)
